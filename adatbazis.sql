-- Film stílusok
CREATE TABLE filmtipus
(
    id  int         NOT NULL AUTO_INCREMENT,
    nev varchar(32) NOT NULL,
    primary key (id)
);

-- Vendégek
CREATE TABLE vendeg
(
    id  int         NOT NULL AUTO_INCREMENT,
    nev varchar(64) NOT NULL,
    primary key (id)
);

-- Filmek
create TABLE film
(
    id          int NOT NULL,
    cim         varchar(64) NOT NULL,
    filmtipusId int NOT NULL,
    jegyar      int NOT NULL,
    primary key (id),
    key filmtipusId (filmtipusId),
    CONSTRAINT FK_film_filmtipusId FOREIGN KEY (filmtipusId)
        REFERENCES filmtipus (id)
);

-- Vetítések
create TABLE vetites
(
    id     int  NOT NULL,
    filmId int  NOT NULL,
    datum  date NOT NULL,
    kezdes time NOT NULL,
    primary key (id),
    key filmId (filmId),
    CONSTRAINT FK_vetites_filmId FOREIGN KEY (filmId)
        REFERENCES film (id)
);

-- Eladott jegyek
create TABLE jegy
(
    id         int NOT NULL AUTO_INCREMENT,
    kedvezmeny int NOT NULL,
    vetitesId     int NOT NULL,
    vendegId   int NOT NULL,
    primary key (id),
    key vetitesId (vetitesId),
    CONSTRAINT FK_jegy_vetitesId FOREIGN KEY (vetitesId)
        REFERENCES vetites (id),
    key vendegId (vendegId),
    CONSTRAINT FK_jegy_vendegId FOREIGN KEY (vendegId)
        REFERENCES vendeg (id)
);


